const TITLES = [
  "في مقام التلقي والشهود",
  "في مقام الافتقار إلى الله",
  "في مقام الهدى والنور",
  "في مقام التوبة والإنابة",
  "في مقام الصبر والثبات",
  "في مقام الشكر والامتنان",
  "في مقام التوكل واليقين",
  "في مقام الخشية والإخبات",
  "في مقام الذكر والمراقبة",
  "في مقام السير إلى الله",
];

const ORDINALS = ["الأول", "الثاني", "الثالث", "الرابع", "الخامس", "السادس", "السابع", "الثامن", "التاسع", "العاشر"];

function makeMajlis(n: number, surah: string, verseNum: number): string {
  const title = TITLES[(n - 1) % TITLES.length];
  const ordinal = ORDINALS[n - 1] ?? `${n}`;

  return `**المجلس ${ordinal}: ${title}**

**١ - كلمات الابتلاء:**
• هذا نصٌّ تجريبيٌّ يمثّل الآية ${verseNum} من سورة ${surah}. ﴿وَهُوَ مَعَكُمْ أَيْنَمَا كُنتُمْ وَاللَّهُ بِمَا تَعْمَلُونَ بَصِيرٌ﴾

**٢ - البيان العام:**
هذا نصٌّ تجريبي للبيان العام في المجلس ${ordinal}. تضع هذه الفقرات القارئ في الجوّ الإيماني للآية الكريمة، وتستحضر عظمة الخطاب الإلهي.

يمتدّ هذا البيان ليشمل السياق التاريخي والمقامي للآية، موضّحاً الصلة الوثيقة بين هذه الآية وما سبقها وما لحق بها في النسيج القرآني.

وتبقى اللغة الأدبية الرفيعة سمةً أصيلة في هذا المقام، إذ لا يُفهَم القرآن إلا بقلبٍ شاهد ولبٍّ حاضر.

**٣ - الهدى المنهاجي:**
وهذه الآية متضمّنة لثلاث رسالات:

**الرسالة الأولى: عنوان الرسالة الأولى**
نصٌّ تجريبي لشرح الرسالة الأولى في المجلس ${ordinal}. يربط هذا الشرح الآية بالحقيقة الإيمانية ويُنزّلها على واقع النفس والمجتمع.

**الرسالة الثانية: عنوان الرسالة الثانية**
نصٌّ تجريبي لشرح الرسالة الثانية. يتناول هذا القسم البعد الاجتماعي والحضاري لمضمون الآية الكريمة.

**الرسالة الثالثة: عنوان الرسالة الثالثة**
نصٌّ تجريبي لشرح الرسالة الثالثة. يختتم هذا القسم الهدى المنهاجي بربط الآية بالكون والمصير والغاية الكبرى من الوجود.

**٤ - مسلك التخلق:**
ومسلك التخلق بهذه الكلمات قائمٌ على وقفةٍ خاصة مع النفس ومساءلتها عمّا إذا كانت تحيا فعلاً في ظلال هذه الآية.

فيا نفسي، هل استشعرتِ عظمة هذا الخطاب الإلهي؟ هل جعلتِ من هذه الآية زاداً يومياً ومرجعاً في كل أمر؟

إن التخلق بهذا الهدى يقتضي مراجعةً يومية وتجديداً مستمراً للنية والعزم، حتى يصير معنى الآية خُلُقاً راسخاً في النفس وسلوكاً مشهوداً في الحياة.`;
}

export function generateMockTadabbur(surah: string, fromVerse: number, toVerse: number): string {
  const count = Math.min(toVerse - fromVerse + 1, 10);
  const parts: string[] = [];
  for (let i = 0; i < count; i++) {
    parts.push(makeMajlis(i + 1, surah, fromVerse + i));
  }
  return parts.join("\n\n---\n\n");
}
